import { create } from 'zustand';
import { persist } from 'zustand/middleware';

// ==================== ТИПЫ ДАННЫХ ====================

// KPI карточки
export interface KpiCard {
  title: string;
  value: string;
  change?: number;
  icon: string;
  color: 'green' | 'red' | 'neutral';
}

// Лента активности
export interface ActivityItem {
  id: string;
  type: 'comment' | 'ban' | 'join' | 'warning' | 'limit' | 'success' | 'error' | 'info';
  message: string;
  timestamp: Date | string;
  campaignId?: string;
  accountId?: string;
}

// Кампании
export interface Campaign {
  id: string;
  name: string;
  status: 'active' | 'paused' | 'error' | 'draft';
  offerType: 'casino' | 'crypto' | 'dating' | 'nutra';
  accountsActive: number;
  accountsTotal: number;
  commentsToday: number;
  revenue: number;
  budget: number;
  budgetSpent: number;
  link?: string;
  autoReplaceOnBan?: boolean;
  maxCommentsPerAccount?: number;
  postingMode?: 'new' | 'old' | 'both';
  delayMin?: number;
  delayMax?: number;
  delayRandom?: boolean;
  useUniqueText?: boolean;
  useDeepSeek?: boolean;
  deepSeekTemperature?: number;
  maxCostPerLead?: number;
  workTimeStart?: string;
  workTimeEnd?: string;
  createdAt: Date | string;
  updatedAt: Date | string;
}

// Аккаунты
